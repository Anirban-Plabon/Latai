# Generate a range of integers from 10 to 100 inclusive
# Note: range(start, stop) stops before the 'stop' value, so we use 101.
numbers = range(10, 101)

# Calculate the sum of these numbers
total_sum = sum(numbers)

# Print the final result to the console
print(f"The sum of numbers from 10 to 100 is: {total_sum}")